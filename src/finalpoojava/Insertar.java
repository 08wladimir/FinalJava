    public void nuevo(int n){
        Menu menu = new Menu();
        Statement consulta = menu.conexion();
        String nombre = txt2.getText();
        String descripcion = txt3.getText();
        try{
        int cantidad = Integer.parseInt(txt4.getText());
        int precio = Integer.parseInt(txt5.getText());
            if(cantidad>0 & precio>0)    {
        JOptionPane.showMessageDialog(null, "El Codigo "+n+" Fue registrado");
        try {
            consulta.executeQuery("INSERT INTO tblproductos "+ "VALUES ("+n+","
                    + " '"+nombre+"', '"+descripcion+"',"
                    + ""+cantidad+", "+precio+")");
        } catch (SQLException ex) {}
            }else{
                JOptionPane.showMessageDialog(null, "El valor que se ingresa en cantidad o precio debe ser mayor que cero");
            }
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "El valor que se ingresa en cantidad o precio debe ser numerico");
        }
    }

